package ru.sbrf.gitcommit.week1;

public class primitives {
    public static void main(String[] args) {
        // Объявление всех типов переменных
        int myInt = 1234;
        short myShort = 3266;
        long myLong = 32456789;

        double myDouble = 2.09;
       // double exp = 2.0 - 1.1;
        float myFloat = 3456.6f;

        boolean myBoolean = true;

        char myChar = 's';
        byte myByte = 99;

        // Вывод переменных в консоль
        System.out.println("Переменная типа Int: " + myInt);
        System.out.println("Переменная типа Short: " + myShort);
        System.out.println("Переменная типа Long: " + myLong);
        System.out.println("Переменная типа Double: " + myDouble);
        System.out.println("Переменная типа Float: " + myFloat);
        System.out.println("Переменная типа Boolean: " + myBoolean);
        System.out.println("Переменная типа Char: " + myChar);
        System.out.println("Переменная типа Byte: " + myByte);
        //System.out.println(exp);
    }
}
